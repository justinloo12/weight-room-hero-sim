"""Grade logged predictions against reality and rebuild SCOREBOARD.md.

Runs the morning after predict_today.py (see PROTOCOL.json). For every
ungraded prediction with a date in the past, the batter's MLB Stats API
game log decides the outcome:

* hr = "1"   — the batter homered on that date
* hr = "0"   — the batter appeared on that date without a home run
* hr = "dnp" — no appearance (scratched, postponed, benched); excluded
               from all scoring, per protocol

Grading never touches the logged probabilities (first write wins). The
scoreboard reports running Brier score against the frozen base-rate
benchmark plus a calibration table; both are recomputed from scratch on
every run, so the file is always a pure function of predictions_log.csv.
Free MLB Stats API only — no odds calls anywhere in this pipeline.
"""

import time
from datetime import datetime
from pathlib import Path
from zoneinfo import ZoneInfo

import numpy as np
import pandas as pd
import requests

ET = ZoneInfo("America/New_York")
LOG_PATH = Path("predictions_log.csv")
SCOREBOARD_PATH = Path("SCOREBOARD.md")

GAME_LOG_URL = ("https://statsapi.mlb.com/api/v1/people/{batter_id}/stats"
                "?stats=gameLog&season={season}&group=hitting&language=en")

# Frozen benchmark (PROTOCOL.json): league P(>=1 HR | >=1 PA) on the
# 2026-03-23..2026-07-02 holdout. The constant predictor to beat.
BASE_RATE = 0.1053

CALIBRATION_BINS = [0.0, 0.05, 0.08, 0.11, 0.15, 0.20, 1.0]
EPS = 1e-6


# ── Pure grading + scoreboard math (unit-tested, no network) ─────────────

def grade_rows(log, outcomes, today):
    """Fill the hr column for past, ungraded rows. Pure.

    `outcomes` maps batter_id -> (played_dates, hr_dates) as sets of
    "YYYY-MM-DD" strings; batters missing from the map are left pending
    (API failure should never mis-grade a row as dnp). Returns
    (graded_frame, n_graded); never modifies rows already graded, rows
    dated today, or future rows.
    """
    log = log.copy()
    n = 0
    hr = log["hr"].astype("string").fillna("")
    for i in log.index:
        if hr[i] != "" or str(log.at[i, "date"]) >= today:
            continue
        bid = int(log.at[i, "batter_id"])
        if bid not in outcomes:
            continue
        played, hrs = outcomes[bid]
        d = str(log.at[i, "date"])
        log.at[i, "hr"] = "1" if d in hrs else ("0" if d in played else "dnp")
        n += 1
    return log, n


def scored_subset(log):
    """Graded rows that count: hr in {0, 1}, probability numeric."""
    m = log["hr"].astype("string").isin(["0", "1"])
    sub = log[m].copy()
    sub["y"] = sub["hr"].astype(int)
    sub["p"] = sub["probability"].astype(float)
    return sub


def brier(y, p):
    y = np.asarray(y, dtype=float)
    p = np.asarray(p, dtype=float)
    return float(np.mean((p - y) ** 2)) if len(y) else float("nan")


def log_loss_score(y, p):
    y = np.asarray(y, dtype=float)
    p = np.clip(np.asarray(p, dtype=float), EPS, 1 - EPS)
    if not len(y):
        return float("nan")
    return float(-np.mean(y * np.log(p) + (1 - y) * np.log(1 - p)))


def calibration_table(sub, bins=CALIBRATION_BINS):
    """[{bin, n, mean_pred, observed}] over graded rows. Pure."""
    rows = []
    for lo, hi in zip(bins[:-1], bins[1:]):
        m = (sub["p"] >= lo) & (sub["p"] < hi)
        if not m.any():
            continue
        rows.append({
            "bin": f"{lo:.0%}–{hi:.0%}" if hi < 1 else f"{lo:.0%}+",
            "n": int(m.sum()),
            "mean_pred": float(sub.loc[m, "p"].mean()),
            "observed": float(sub.loc[m, "y"].mean()),
        })
    return rows


def render_scoreboard(log, generated_at, base_rate=BASE_RATE):
    """SCOREBOARD.md content — a pure function of the log. Pure."""
    sub = scored_subset(log)
    pending = int((log["hr"].astype("string").fillna("") == "").sum())
    dnp = int((log["hr"].astype("string") == "dnp").sum())
    lines = [
        "# Live Forecasting Scoreboard",
        "",
        "**Auto-generated by `grade_predictions.py` — do not edit.** "
        "Frozen model `gbm_v3` graded daily against real outcomes "
        "(protocol: [PROTOCOL.json](PROTOCOL.json)).",
        "",
        f"_Last updated: {generated_at}_",
        "",
        f"- Graded predictions: **{len(sub)}** "
        f"({int(sub['y'].sum()) if len(sub) else 0} HR games, "
        f"{sub['y'].mean():.4f} observed rate)" if len(sub) else
        "- Graded predictions: **0**",
        f"- Pending: {pending} | Did not play (excluded): {dnp}",
        "",
    ]
    if len(sub):
        b_model = brier(sub["y"], sub["p"])
        b_base = brier(sub["y"], np.full(len(sub), base_rate))
        skill = 1.0 - b_model / b_base if b_base else float("nan")
        lines += [
            "## Running score",
            "",
            "| Predictor | Brier ↓ | Log loss ↓ | Mean pred |",
            "|---|---|---|---|",
            f"| gbm_v3 (frozen) | **{b_model:.5f}** | {log_loss_score(sub['y'], sub['p']):.5f} | {sub['p'].mean():.4f} |",
            f"| Base rate ({base_rate}) | {b_base:.5f} | {log_loss_score(sub['y'], np.full(len(sub), base_rate)):.5f} | {base_rate:.4f} |",
            "",
            f"**Skill vs base rate: {skill:+.4f}** "
            f"(success criterion: > 0 after ≥ 1,000 graded predictions)",
            "",
            "## Calibration",
            "",
            "| Predicted bin | n | Mean pred | Observed |",
            "|---|---|---|---|",
        ]
        for r in calibration_table(sub):
            lines.append(f"| {r['bin']} | {r['n']} | {r['mean_pred']:.4f} | {r['observed']:.4f} |")
        lines.append("")
    else:
        lines += ["No graded predictions yet — check back after the first "
                  "full predict → grade cycle.", ""]
    return "\n".join(lines)


# ── Network + IO glue ─────────────────────────────────────────────────────

def fetch_outcomes(batter_id, season):
    """(played_dates, hr_dates) for one batter-season from the game log."""
    resp = requests.get(GAME_LOG_URL.format(batter_id=batter_id, season=season),
                        timeout=15)
    resp.raise_for_status()
    played, hrs = set(), set()
    for group in resp.json().get("stats", []):
        for split in group.get("splits", []):
            d = split.get("date", "")
            played.add(d)
            if split.get("stat", {}).get("homeRuns", 0) > 0:
                hrs.add(d)
    return played, hrs


def main():
    if not LOG_PATH.exists():
        print("predictions_log.csv not found — nothing to grade.")
        return
    log = pd.read_csv(LOG_PATH, dtype={"hr": "string"}, keep_default_na=False)
    today = datetime.now(ET).strftime("%Y-%m-%d")

    pending = log[(log["hr"].astype("string").fillna("") == "")
                  & (log["date"].astype(str) < today)]
    need = sorted({(int(b), int(str(d)[:4]))
                   for b, d in zip(pending["batter_id"], pending["date"])})
    print(f"Grading {len(pending)} predictions ({len(need)} batter-seasons)...")

    outcomes = {}
    for bid, season in need:
        try:
            outcomes[bid] = fetch_outcomes(bid, season)
        except Exception as e:
            print(f"  game log failed for {bid}: {e}")
        time.sleep(0.15)

    log, n = grade_rows(log, outcomes, today)
    log.to_csv(LOG_PATH, index=False)

    board = render_scoreboard(log, datetime.now(ET).strftime("%Y-%m-%d %H:%M ET"))
    SCOREBOARD_PATH.write_text(board)
    print(f"Graded {n} rows. Wrote {SCOREBOARD_PATH}.")


if __name__ == "__main__":
    main()
